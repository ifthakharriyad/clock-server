// Clock is a concurrent TCP server that periodically writes the time
package main

import (
	"fmt"
	"io"
	"log"
	"net"
	"time"
)

func main() {
	address := fmt.Sprint("localhost:", 8000)
	listener, err := net.Listen("tcp", address)
	if err != nil {
		log.Fatal(err)
	}
	for {
		conn, err := listener.Accept()
		if err != nil {
			log.Print(err)
		}
		go handleConn(conn)
	}
}

func handleConn(c net.Conn) {
	defer c.Close()
	for {
		_, err := io.WriteString(c, time.Now().Format("15:04:05\n"))
		if err != nil {
			return
		}
		time.Sleep(1 * time.Second)
	}
}
